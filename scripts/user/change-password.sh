#!/bin/bash

API="http://localhost:4741"
URL_PATH="/change-password"
ID=
TOKEN=
OLDPW=
NEWPW=

curl "${API}${URL_PATH}/${ID}" \
  --include \
  --request PATCH \
  --header "Authorization: Token token=${TOKEN}" \
  --header "Content-Type: application/json" \
  --data '{
    "passwords": {
      "old": "'"${OLDPW}"'",
      "new": "'"${NEWPW}"'"
    }
  }'

echo
